	import java.util.Scanner;

	public class Assignment {
		public static void main(String[] args) throws IOExeption{
			int num1=10;
			num1+=5;//action of num1=num1+5
			System.out.println("The output of num1+=5 is"+num1);
			num1-=5;//action of num1=num1-5
			System.out.println("The output of num1-=5 is"+num1);
			num1*=5;//action of num1=num1*5
			System.out.println("The output of num1*=5 is"+num1);
			num1/=5;//action of num1=num1/5
			System.out.println("The output of num1/=5 is"+num1);
			num1%=5;//action of num1=num1%5
			System.out.println("The output of num1%=5 is"+num1);
			
		}

	}

